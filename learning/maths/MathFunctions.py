import math


x = 2.9
print(round(x))
x = 3.5
print(round(x))
x = 4.4
print(round(x))
x = 4.456
print(round(x))
# Rounds to 1 decimal place
print(round(x, 1))
# Rounds to 2 decimal places
print(round(x, 2))

print(abs(2.9))
# abs or Absolute always returns positive number
print(abs(-2.9))

print(round(math.pi, 2))
print(math.ceil(2.9))
print(math.floor(2.9))
print(math.factorial(5))
print(round(math.sin(1.571)))
print(round(math.degrees(1.571)))
